    public function checkFive(string|int|float $sInput, bool $bDetails = false):array
    {
        $arResult["bSuccess"] = false;


        if (array_key_exists($sInput, self::FIVE_VARIANTS)) {
            $arResult["bSuccess"] = true;

            if ($bDetails){
                $arResult["sDescription"] = self::FIVE_VARIANTS[$sInput];
                $arResult["sInputType"] = gettype($sInput);

            }
        }

        return $arResult;

    }

    public function getAllFiveVariants(): array
    {
        return self::FIVE_VARIANTS;
